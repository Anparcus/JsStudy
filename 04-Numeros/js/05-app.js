let puntaje = 5;

//Suma
puntaje++; //Aumenta despues de llamar a puntaje
++puntaje; //Agrega antes de llamar a puntaje e imprime el resultado ya hecha la operacion

//Resta
puntaje--;
--puntaje;

//Se pueden hacer saltos de un numero dado
puntaje -= 3;
puntaje += 3;

console.log(puntaje);